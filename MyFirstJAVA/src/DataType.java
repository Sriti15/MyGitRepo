
public class DataType {

	public static void main(String[] args) {
		
		// primitive data types language which are int, char, long, short, float, double, boolean, string.
		// to save a whole number use "int"( interger)
		
		int myNum = 50;
		System.out.println(myNum);
		
		double myRate = 4.75;
		System.out.println(myRate);
				
		long myMoney = 806565654565l;
		System.out.println(myMoney);
		
		float myAccount = 80.56863875685856f;
		System.out.println(myAccount);
		
		char myChar =  'U';
		System.out.println(myChar);

		boolean myAnswer = true;
		System.out.println("myAnswer");
		
		String myName = "ProSmart";
		String myPassword = "abcde12";
		System.out.println(myName + myPassword);
		System.out.println(myName + " " + myPassword);
		
		String myLine = "I love Java and automation";
		System.out.println(myLine);
		
		// NON PRIMITIVE
		
	   
	}
		
	}

